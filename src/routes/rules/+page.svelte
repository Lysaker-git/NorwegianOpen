            <strong class="font-semibold">Petition Down:</strong> To compete one level lower than your current points indicate (primary role only), submit a formal petition request to the Chief Judge two weeks before event start. A committee including the Chief Judge will review your WSDC record. Approval is granted on an event-by-event basis. (
                <a href="https://www.worldsdc.com/wp-content/uploads/2023/12/WSDC-Jack-and-Jill-petition_vF_Oct-2023.pdf" target="_blank" rel="noopener noreferrer" class="link-style">Petition down Form</a>
            )
        </p>
        <p class="mb-4">
            <strong class="font-semibold">Petition Up:</strong> To compete one level higher (primary role only), petitions must generally be submitted directly to the WSDC Chief Judges Committee well in advance of the event (
                <a href="https://www.worldsdc.com/level-up-petition/" target="_blank" rel="noopener noreferrer" class="link-style">Petition up Form</a>
            ), often requiring video submissions. Approved petitions up may be valid for a set period.
        </p>
        <p class="mb-4">
            <strong class="font-semibold">No Petitions for Secondary Role:</strong> Petitions are not accepted for changing levels in your secondary role.
        </p>
        <p class="mb-4">
            <strong class="font-semibold">Pending Points:</strong> If you qualify for a higher division due to recent placements not yet reflected in the WSDC registry, submit a petition with details of the event, placement, tier, and event contact for verification.
        </p>
        <p class="mb-4">
            <strong class="font-semibold">Decisions Final:</strong> All decisions regarding petitions made by the Chief Judge or competition officials are final. Be prepared to compete in your originally qualified division if your petition is denied.
        </p>
