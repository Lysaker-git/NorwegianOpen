            V. Dance Requirements & Judging
          </h2>
          <p class="mb-4"><strong class="font-semibold">Swing Content:</strong> Competitions are focused on swing dancing. Judges expect to see recognizable swing content (<code class="code-style">[minimum 80-90%]</code>). Jack & Jill divisions primarily feature West Coast Swing.</p>
          <p class="mb-4"><strong class="font-semibold">Lead/Follow:</strong> Jack & Jill and Strictly Swing competitions emphasize spontaneous, lead/follow dancing. Pre-choreographed routines or significant amounts of choreographed sequences are not permitted in these divisions.</p>
          <p class="mb-4"><strong class="font-semibold">Connection:</strong> Maintain appropriate physical dance connection with your partner throughout the dance, except during spins, turns, short breakaways, etc., that are natural parts of the dance.</p>
          <div class="mb-4">
            <strong class="font-semibold block mb-1">Partner Weight Support & Lifts:</strong>
            <ul class="list-disc pl-6 space-y-2">
