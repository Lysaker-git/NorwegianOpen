                <li>Lifts, aerials, and acrobatic moves where both of a partner's feet leave the floor are <strong class="font-bold text-red-600 uppercase">NOT ALLOWED</strong> in most divisions (Newcomer, Novice, Intermediate J&J and Strictly).</li>
                <li><em class="font-medium">Exception:</em> Limited lifts (e.g., below the shoulder) may be permitted <em class="font-medium">only</em> in specified divisions like <code class="code-style">(All-Star J&J, Open Strictly Swing)</code>. Confirm specific divisional rules.</li>
                <li>Any move deemed unsafe by the judges may result in a penalty. When in doubt, prioritize safety and leave it out.</li>
            </ul>
          </div>
          <p class="mb-4"><strong class="font-semibold">Attire & Presentation:</strong> Dress appropriately for competition. While costumes are generally disallowed (except for specific Routine divisions), neat, complementary, or matching outfits suitable for swing dancing are encouraged, especially for Strictly Swing. Avoid overly casual wear like jeans. Attire and presentation should be tasteful and respectful, suitable for an all-ages audience. Check with the Chief Judge beforehand if you have questions about attire acceptability.</p>
          <div class="mb-4">
            <strong class="font-semibold block mb-1">Judging Criteria:</strong> Competitors are judged on a relative basis using criteria that include:
            <ol class="list-decimal pl-6 space-y-1 mt-2">
                <li><strong class="font-medium">Timing:</strong> Dancing on time with the music, including rhythm and syncopations.</li>
                <li><strong class="font-medium">Teamwork:</strong> Connection, lead/follow interaction, partnership, and mutual respect.</li>
                <li><strong class="font-medium">Technique:</strong> Execution of footwork, body movement, balance, control, and spins/turns.</li>
                <li><strong class="font-medium">Content/Choreography:</strong> For social divisions, the spontaneous selection and execution of patterns appropriate to the dance style and music.</li>
                <li><strong class="font-medium">Musicality:</strong> Interpretation of the music's style, tempo, phrasing, and mood.</li>
                <li><strong class="font-medium">Presentation:</strong> Overall appearance, showmanship, and engagement, demonstrating positive chemistry and sportsmanship.</li>
            </ol>
          </div>
